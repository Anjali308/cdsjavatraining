public class nestedwhile {
    public static void main(String[] args) {
        int i = 1;
        while(i < 5){
            System.out.println("I am a girl");
            i++;
            int j=4;
            while(j<5){
                System.out.println("I am a boy");
                j++;
            }
        }
        
    }
        
    }
    

